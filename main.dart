void main () {
  // int data type,used for integers
  int age = 45;
  print("Age: $age");

  // double data type, for storing doubles (decimals)
  double weight = 67.4;
  print ("Weight: $weight");

  // String data type, for storing strings
  String name = 'Master Zacs';
  print('Name: $name');

  // List data type, for storing lists (array)
  List<int> numbers = [1, 2, 3, 4, 5];
  print('Numbers: $numbers');

  // Map data type (key-value pairs)
  Map<String, dynamic> person = {
    'name': 'Carol Shyne',
    'age': 25,
    'location': 'Kayole',
    'is_driving': true
  };
  print('Person: $person');
}